		##Заметки по лекции

		Алгоритм со сложностью $f(n)$ называется:

		* Если $f=\Theta\left(\log^k n\right)$: логарифмическим при $k=1$, полилогарифмическим при $k>1$.
		* Если $f=\Theta(n)$: линейным.
		* Если $f=\Theta\left(n\log^k n\right)$: linearithmic при $k=1$, квазилинейным при $k>1$.
		* Если $f=\Theta\left(n^k\right)$: полиномиальным, при $k=2$ квадратичным.
		* Если $f=\Theta\left(2^{n^k}\right)$: экспоненциальным.
